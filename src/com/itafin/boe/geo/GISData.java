package com.itafin.boe.geo;

public class GISData {
  
  public String latax_dist;
  public String cd_no;
  public String taps_cd;
  public boolean in_city;
  public Double latitude;
  public Double longitude;
  
  public String getLatax_dist() {
    return latax_dist;
  }
  public void setLatax_dist(String latax_dist) {
    this.latax_dist = latax_dist;
  }
  public String getCd_no() {
    return cd_no;
  }
  public void setCd_no(String cd_no) {
    this.cd_no = cd_no;
  }
  public String getTaps_cd() {
    return taps_cd;
  }
  public void setTaps_cd(String taps_cd) {
    this.taps_cd = taps_cd;
  }
  public boolean getIn_city() {
    return in_city;
  }
  public void setIn_city(boolean in_city) {
    this.in_city = in_city;
  }
  public Double getLatitude() {
    return latitude;
  }
  public void setLatitude(Double latitude) {
    this.latitude = latitude;
  }
  public Double getLongitude() {
    return longitude;
  }
  public void setLongitude(Double longitude) {
    this.longitude = longitude;
  }
  
}
